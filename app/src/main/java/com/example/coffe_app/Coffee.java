package com.example.coffe_app;

public class Coffee {



    double toppings;
    double bagelwithcream;
    double bagelwithbutter;
    double bagelwithpeach;




    public Coffee() {
    }

    public double getToppings() {
        return toppings;
    }

    public void setToppings(double toppings) {
        this.toppings = toppings;
    }

    public double getBagelwithcream() {
        return bagelwithcream;
    }

    public void setBagelwithcream(double bagelwithcream) {
        this.bagelwithcream = bagelwithcream;
    }

    public double getBagelwithbutter() {
        return bagelwithbutter;
    }

    public void setBagelwithbutter(double bagelwithbutter) {
        this.bagelwithbutter = bagelwithbutter;
    }

    public double getBagelwithpeach() {
        return bagelwithpeach;
    }

    public void setBagelwithpeach(double bagelwithpeach) {
        this.bagelwithpeach = bagelwithpeach;
    }
}


